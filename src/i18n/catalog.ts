const catalogs: Record<Exclude<Lang, 'fr'>, Record<string, string>> = { en, ar }

export function hasTranslation(lang: Exclude<Lang, 'fr'>, source: string) {
  return Object.prototype.hasOwnProperty.call(catalogs[lang], source)
}

export function translate(lang: Lang, source: string, variables?: Variables): string {
  let value = source
  if (lang !== 'fr') {
    const translated = catalogs[lang][source]
    if (translated !== undefined) {
      value = translated
    } else if (import.meta.env.DEV) {
      console.warn('[i18n] Missing translation', { lang, source })
    }
  }
  if (!variables) return value
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(variables[key] ?? `{${key}}`))
}
