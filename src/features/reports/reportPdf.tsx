function publicAssetPath(path: string) {
  if (typeof window !== 'undefined') return path
  const localPath = decodeURIComponent(new URL(`../../../public${path}`, import.meta.url).pathname)
  return localPath.replace(/^\/([A-Za-z]:\/)/, '$1')
}

