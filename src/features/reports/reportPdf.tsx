export interface DeliveryReportPdfData {
  report: DeliveryReport
  request: ServiceRequest
  garage: Garage | null
  lang: Lang
  attachments?: ServiceRequestAttachment[]
  centerName?: string | null
  financialSummary?: string[]
  attachmentPreviews?: Record<string, string>
}

function renderReportSection({ title, values, fallback, styles }: {
  title: string
  values: Array<string | null | undefined>
  fallback: string
  styles: ReturnType<typeof reportStyles>
}) {
  const visible = values.filter((value): value is string => !!value?.trim())
  return (
    <View style={styles.section} minPresenceAhead={24}>
      <Text style={styles.label}>{title}</Text>
      {visible.length ? visible.map((value, index) => (
        <View key={`${title}-${index}`} style={styles.listRow}>
          <Text style={styles.listBullet}>•</Text>
          <Text style={styles.listItem}>{value}</Text>
        </View>
      )) : <Text style={styles.value}>{fallback}</Text>}
    </View>
  )
}

function renderFactRow({ label, value, technical, styles }: {
  label: string
  value: string
  technical?: boolean
  styles: ReturnType<typeof reportStyles>
}) {
  return (
    <View style={styles.factRow}>
      <Text style={styles.factLabel}>{label}</Text>
      <Text style={technical ? styles.technical : styles.dateValue}>{value}</Text>
    </View>
  )
}

function renderTechnicalSection({ title, values, fallback, styles }: {
  title: string
  values: string[]
  fallback: string
  styles: ReturnType<typeof reportStyles>
}) {
  return (
    <View style={styles.section} wrap={false}>
      <Text style={styles.label}>{title}</Text>
      {values.length
        ? values.map((value) => <Text key={value} style={styles.technical}>{value}</Text>)
        : <Text style={styles.value}>{fallback}</Text>}
    </View>
  )
}

export function deliveryReportDocument({
  report,
  request,
  garage,
  lang,
  attachments = [],
  centerName,
  financialSummary = [],
  attachmentPreviews = {},
}: DeliveryReportPdfData) {
  const text = labels[lang]
  const rtl = lang === 'ar'
  const styles = reportStyles(rtl, garage?.accent_color || '#0f766e')
  const customer = snapshotText(report.customer_snapshot, ['name', 'full_name'], text.noValue)
  const vehicle = snapshotText(report.vehicle_snapshot, ['label', 'vehicle_label'], request.vehicle_label || text.noValue)
  const registration = snapshotText(report.vehicle_snapshot, ['registration', 'plate'], '')
  const authorizedAttachments = attachments.filter((item) => report.authorized_attachment_ids.includes(item.id))
  const numberFormat = new Intl.NumberFormat(lang === 'fr' ? 'fr-FR' : lang === 'ar' ? 'ar-MA' : 'en-GB')
  const localizedDate = (value: string) => rtl ? `\u200F${value}\u200F` : value
  return (
    <Document title={`${text.title} ${report.report_number}`}>
      <Page size="A4" style={styles.page}>
        <View style={styles.header}>
          <View>
            <Text style={styles.garage}>{garage?.name ?? 'Clikarage'}</Text>
            <Text style={styles.value}>{garage?.address ?? ''}</Text>
            <Text style={styles.technical}>{[garage?.phone, garage?.email].filter(Boolean).join(' · ')}</Text>
          </View>
          <View>
            <Text style={styles.title}>{text.title}</Text>
            <Text style={styles.technical}>{report.report_number}</Text>
            <Text style={styles.dateValue}>{localizedDate(shortDate(report.finalized_at ?? report.updated_at, lang))}</Text>
          </View>
        </View>

        {centerName && (
          <View style={styles.section}>
            <Text style={styles.label}>{text.center}</Text>
            <Text style={styles.value}>{centerName}</Text>
          </View>
        )}

        <View style={styles.grid}>
          <View style={styles.half}><Text style={styles.label}>{text.client}</Text><Text style={styles.value}>{customer}</Text></View>
          <View style={styles.half}>
            <Text style={styles.label}>{text.vehicle}</Text>
            <Text style={styles.value}>{vehicle}</Text>
            {registration && <Text style={styles.technical}>{registration}</Text>}
          </View>
        </View>
        <View style={styles.grid}>
          <View style={styles.half}>
            <Text style={styles.label}>{text.dates}</Text>
            {renderFactRow({ label: text.checkIn, value: report.checked_in_at ? localizedDate(dateTime(report.checked_in_at, lang)) : text.noValue, styles })}
            {renderFactRow({ label: text.delivered, value: report.delivered_at ? localizedDate(dateTime(report.delivered_at, lang)) : text.noValue, styles })}
          </View>
          <View style={styles.half}>
            <Text style={styles.label}>{text.mileage}</Text>
            {renderFactRow({
              label: text.entry,
              value: report.entry_mileage === null ? text.noValue : `${numberFormat.format(report.entry_mileage)} km`,
              technical: report.entry_mileage !== null,
              styles,
            })}
            {renderFactRow({
              label: text.exit,
              value: report.exit_mileage === null ? text.noValue : `${numberFormat.format(report.exit_mileage)} km`,
              technical: report.exit_mileage !== null,
              styles,
            })}
          </View>
        </View>

        {renderReportSection({ title: text.requested, values: report.requested_work, fallback: text.noValue, styles })}
        {renderReportSection({ title: text.diagnosis, values: [report.diagnostic_summary], fallback: text.noValue, styles })}
        {renderReportSection({ title: text.completed, values: report.completed_work, fallback: text.noValue, styles })}
        {renderReportSection({ title: text.accepted, values: report.accepted_recommendations, fallback: text.noValue, styles })}
        {renderReportSection({ title: text.deferred, values: report.deferred_recommendations, fallback: text.noValue, styles })}
        {renderReportSection({ title: text.parts, values: report.parts, fallback: text.noValue, styles })}
        {renderTechnicalSection({ title: text.amounts, values: financialSummary, fallback: text.noValue, styles })}
        <View style={styles.section} minPresenceAhead={110}>
          <Text style={styles.label}>{text.attachments}</Text>
          {authorizedAttachments.length ? (
            <View style={styles.attachmentGrid}>
              {authorizedAttachments.map((item) => (
                <View key={item.id} style={styles.attachmentCard} wrap={false}>
                  {attachmentPreviews[item.id]
                    ? <Image src={attachmentPreviews[item.id]} style={styles.attachmentImage} />
                    : <View style={styles.attachmentPlaceholder}><Text>{text.photo}</Text></View>}
                  <Text style={styles.attachmentName}>{item.file_name}</Text>
                </View>
              ))}
            </View>
          ) : <Text style={styles.value}>{text.noValue}</Text>}
        </View>
        {renderReportSection({ title: text.observations, values: [report.observations], fallback: text.noValue, styles })}
        <View style={styles.section} minPresenceAhead={24}>
          <Text style={styles.label}>{text.next}</Text>
          {report.next_due_date && <Text style={styles.value}>{localizedDate(shortDate(report.next_due_date, lang))}</Text>}
          {report.next_due_mileage !== null && <Text style={styles.technical}>{numberFormat.format(report.next_due_mileage)} km</Text>}
          {!report.next_due_date && report.next_due_mileage === null && <Text style={styles.value}>{text.noValue}</Text>}
        </View>
        {renderReportSection({ title: text.warranty, values: [report.warranty_terms], fallback: text.noValue, styles })}
        {renderReportSection({ title: text.validation, values: [report.final_validation], fallback: text.noValue, styles })}
        <Text
          fixed
          style={styles.footer}
          render={({ pageNumber, totalPages }) => `${text.legal} · ${text.page} ${pageNumber}/${totalPages}`}
        />
      </Page>
    </Document>
  )
}

export async function renderDeliveryReportPdfBlob(data: DeliveryReportPdfData) {
  return pdf(deliveryReportDocument(data)).toBlob()
}

export async function downloadDeliveryReportPdf(data: DeliveryReportPdfData) {
  const blob = await renderDeliveryReportPdfBlob(data)
  const url = URL.createObjectURL(blob)
  const anchor = document.createElement('a')
  anchor.href = url
  anchor.download = `${data.report.report_number}.pdf`
  document.body.appendChild(anchor)
  anchor.click()
  anchor.remove()
  setTimeout(() => URL.revokeObjectURL(url), 2000)
}
